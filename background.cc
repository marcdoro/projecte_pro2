void Background::paint_object(pro2::Window&                        window,
                              const std::vector<std::vector<int>>& sprite,
                              float                                factor) const {
    pro2::Pt camera_pos = window.topleft();
    int      sprite_width = sprite[0].size();
    int      sprite_height = sprite.size();

    for (int i = 0; i < 5; i++) {
        int object_x = 75 + 700 * i - static_cast<int>(camera_pos.x * factor);
        int object_y = 10 - static_cast<int>(camera_pos.y * factor);

        if (object_x > window.width() or object_x + sprite_width < 0 or
            object_y > window.height() or object_y + sprite_height < 0) {
            continue;
        }

        pro2::Pt screen_pos = {object_x + camera_pos.x, object_y + camera_pos.y};
        paint_sprite(window, screen_pos, sprite, false);
    }
}

void Background::paint(pro2::Window& window) const {
    pro2::Pt cam = window.topleft();

    window.clear(0x1f1975);

    for (const auto& p : random_positions_) {
        pro2::paint_sprite(window, {cam.x + p.first.x, cam.y + p.first.y}, stars_sprites_[p.second],
                           false);
    }

    paint_object(window, planet_, planet_factor);
}